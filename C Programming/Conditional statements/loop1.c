#include<stdio.h>
int main()
{
	int i;
	//for loop
	printf("\nFor loop := ");
	for(i=1;i<=10;i++)
	{
		printf("\nTOPS Technologies");
	}
	
	//while loop
	printf("\nWhile loop := ");
	i=0;
	while(i<10)
	{
		printf("\nTOPS Technologies");
		i++;
	}
	
	//do while loop
	printf("\nDo while loop := ");
	i=1;
	do
	{
		printf("\nTOPS Technologies");
		i++;
	}while(i<=10);
	
	return 0;
}
