DECLARE
	X NUMBER := 10;
	Y NUMBER := 20;
BEGIN
	DBMS_OUTPUT.PUT_LINE('THE VALUE OF X = '||X);
	DBMS_OUTPUT.PUT_LINE('THE VALUE OF Y = '||Y);
END;

DECLARE -- FIRST BLOCK
X NUMBER := 10;
BEGIN
	DECLARE -- SECOND BLOCK
	Y NUMBER := 20;
	BEGIN
		DECLARE -- THIRD BLOCK
		Z NUMBER := 30;
		BEGIN
            DBMS_OUTPUT.PUT_LINE('THIRD BLOCK := ');
			DBMS_OUTPUT.PUT_LINE('THE VALUE OF X = '||X);
			DBMS_OUTPUT.PUT_LINE('THE VALUE OF Y = '||Y);
			DBMS_OUTPUT.PUT_LINE('THE VALUE OF Z = '||Z);
		END; -- THIRD BLOCK ENDED
		DBMS_OUTPUT.PUT_LINE('SECOND BLOCK := ');
		DBMS_OUTPUT.PUT_LINE('THE VALUE OF X = '||X);
		DBMS_OUTPUT.PUT_LINE('THE VALUE OF Y = '||Y);
	END; -- SECOND BLOCK ENDED
	DBMS_OUTPUT.PUT_LINE('FIRST BLOCK := ');
	DBMS_OUTPUT.PUT_LINE('THE VALUE OF X = '||X);
END; -- FIRST BLOCK ENDED

DECLARE
	NUM1 NUMBER := 30;
	NUM2 NUMBER := 20;
BEGIN
	IF NUM1>NUM2 THEN
		DBMS_OUTPUT.PUT_LINE(NUM1||' IS THE BIGGEST NUMBER');
	ELSE
		DBMS_OUTPUT.PUT_LINE(NUM2||' IS THE BIGGEST NUMBER');
	END IF;
END;

DECLARE
	NUM1 NUMBER := 40;
	NUM2 NUMBER := 60;
	NUM3 NUMBER := 30;
BEGIN
	IF NUM1>NUM2 AND NUM1>NUM3 THEN
		DBMS_OUTPUT.PUT_LINE(NUM1||' IS THE BIGGEST NUMBER');
	ELSIF NUM2>NUM1 AND NUM2>NUM3 THEN
		DBMS_OUTPUT.PUT_LINE(NUM2||' IS THE BIGGEST NUMBER');
	ELSE
		DBMS_OUTPUT.PUT_LINE(NUM3||' IS THE BIGGEST NUMBER');
	END IF;
END;

-- SIMPLE LOOP
DECLARE 
	I NUMBER := 0;
BEGIN
	LOOP
		DBMS_OUTPUT.PUT_LINE('TOPS TECHNOLOGIES');
		I := I + 1;
		IF I = 5 THEN
			EXIT;
		END IF;
	END LOOP;
END;

-- SIMPLE LOOP
DECLARE 
	I NUMBER := 0;
BEGIN
	LOOP
		DBMS_OUTPUT.PUT_LINE('TOPS TECHNOLOGIES');
		I := I + 1;
		EXIT WHEN I = 5;
	END LOOP;
END;

-- FOR LOOP
BEGIN
	FOR X IN 1..10 LOOP
		DBMS_OUTPUT.PUT_LINE(X);
	END LOOP;
END;

-- FOR LOOP
DECLARE
	NUM NUMBER := 1;
BEGIN
	FOR X IN 1..5 LOOP
		DBMS_OUTPUT.PUT_LINE(NUM);
		NUM := NUM + 1;
	END LOOP;
END;

-- WHILE LOOP
DECLARE
	X NUMBER := 0;
BEGIN
	WHILE X<5 LOOP
		DBMS_OUTPUT.PUT_LINE(X+1||'. TOPS TECHNOLOGIES');
		X := X + 1;
	END LOOP;
END;